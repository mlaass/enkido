BitmapFont::~BitmapFont() {
    shutdown();
}

bool BitmapFont::init(SDL_Renderer* renderer) {
    renderer_ = renderer;

    // Create a texture atlas for all characters
    // Layout: all glyphs in a horizontal row
    int atlas_width = GLYPH_WIDTH * FONT_NUM_CHARS;
    int atlas_height = GLYPH_HEIGHT;

    // Create surface
    SDL_Surface* surface = SDL_CreateRGBSurfaceWithFormat(
        0, atlas_width, atlas_height, 32, SDL_PIXELFORMAT_RGBA8888);
    if (!surface) {
        return false;
    }

    // Fill with transparent
    SDL_FillRect(surface, nullptr, SDL_MapRGBA(surface->format, 0, 0, 0, 0));

    // Render each character
    auto* pixels = static_cast<std::uint32_t*>(surface->pixels);
    int pitch = surface->pitch / 4;

    for (int c = 0; c < FONT_NUM_CHARS; ++c) {
        int base_x = c * GLYPH_WIDTH;
        for (int y = 0; y < GLYPH_HEIGHT; ++y) {
            std::uint8_t row = font_data[c][y];
            for (int x = 0; x < GLYPH_WIDTH; ++x) {
                if (row & (0x80 >> x)) {
                    pixels[y * pitch + base_x + x] =
                        SDL_MapRGBA(surface->format, 255, 255, 255, 255);
                }
            }
        }
    }

    // Create texture from surface
    texture_ = SDL_CreateTextureFromSurface(renderer, surface);
    SDL_FreeSurface(surface);

    if (!texture_) {
        return false;
    }

    // Enable blending for color modulation
    SDL_SetTextureBlendMode(texture_, SDL_BLENDMODE_BLEND);

    return true;
}

void BitmapFont::shutdown() {
    if (texture_) {
        SDL_DestroyTexture(texture_);
        texture_ = nullptr;
    }
    renderer_ = nullptr;
}

void BitmapFont::draw_char(char c, int x, int y, SDL_Color color) {
    if (!texture_ || c < FONT_FIRST_CHAR || c > FONT_LAST_CHAR) {
        return;
    }

    int index = c - FONT_FIRST_CHAR;

    SDL_Rect src = {index * GLYPH_WIDTH, 0, GLYPH_WIDTH, GLYPH_HEIGHT};
    SDL_Rect dst = {x, y, GLYPH_WIDTH, GLYPH_HEIGHT};

    SDL_SetTextureColorMod(texture_, color.r, color.g, color.b);
    SDL_SetTextureAlphaMod(texture_, color.a);
    SDL_RenderCopy(renderer_, texture_, &src, &dst);
}

void BitmapFont::draw_string(const char* str, int x, int y, SDL_Color color) {
    if (!str) return;
    draw_string(str, std::strlen(str), x, y, color);
}

void BitmapFont::draw_string(const char* str, std::size_t len, int x, int y, SDL_Color color) {
    if (!str || !texture_) return;

    int cursor_x = x;
    for (std::size_t i = 0; i < len; ++i) {
        char c = str[i];
        if (c >= FONT_FIRST_CHAR && c <= FONT_LAST_CHAR) {
            draw_char(c, cursor_x, y, color);
        }
        cursor_x += GLYPH_WIDTH;
    }
}

int BitmapFont::string_width(const char* str) const {
    if (!str) return 0;
    return static_cast<int>(std::strlen(str)) * GLYPH_WIDTH;
}

